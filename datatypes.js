// variable, constants

// var can be assigned and reassigned 
var score = 34;
var score = 56;
console.log(score);

// let can be declared only one time but value can be changed
let num = 10;
// let num = 20;  if we do this it will give an error
num = 20;
console.log(num);

// const can be declared once and its value is unchangeable
// if a const is declared it must be assigned the value at the time of creation 
// otherwise it will give an error in the console
const number = 55;
// const number = 60; this will give an error no redeclaration
// number = 65; this is giving error in console because the value is being re assigned and it is not allowed in const
console.log(number);


